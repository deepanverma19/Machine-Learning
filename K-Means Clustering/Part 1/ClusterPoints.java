public class ClusterPoints {
    private int clusterID;//
    private float xCoordinate;
    private float yCoordinate;

    //This function returns the clusterID
    public int getClusterID() {
        return clusterID;
    }

    //This function sets the clusterID
    public void setClusterID(int clusterID) {
        this.clusterID = clusterID;
    }

    //This function returns the xCoordinate
    public float getxCoordinate() {
        return xCoordinate;
    }

    //This function sets the xCoordinate
    public void setxCoordinate(float xCoordinate) {
        this.xCoordinate = xCoordinate;
    }

    //This function returns the yCoordinate
    public float getyCoordinate() {
        return yCoordinate;
    }

    //This function set the yCoordinate
    public void setyCoordinate(float yCoordinate) {
        this.yCoordinate = yCoordinate;
    }
}
