/*
This JAVA class uses Long id and String text as attributes
Implemented getters for both
getId() and getText()
 */
public class Data {

    //Using Long id for id and String text for the text
    public Long id;
    public String text;

    //getId() returns the id
    public Long getId(){
        return this.id;
    }

    //getText() returns the text
    public String getText(){
        return this.text;
    }

}
